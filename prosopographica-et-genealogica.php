                                            <p>Resumé</p>
                                            <p>Lorsque l’on regarde le tableau généalogique des Landulfides dressé par N. Cilento à la fin de
                                                son édition commentée de la chronique de la dynastie capuane, on est frappé par le petit nombre
                                                de femmes y figurant : 14 sur 71 personnages. Or, une étude approfondie des sources permet
                                                d’en identifier une trentaine d’autres. Cet exemple illustre le peu de place faite aux femmes des
                                                lignées régnantes d’Italie du sud dans la littérature historique. Ce livre à pour objet de les
                                                identifier, de les sortir de l’ombre et d’en faire un relevé prosopographique.
                                                Le cadre géographique du projet est l’Italie méridionale lombarde située au sud des Abbruzes
                                                spolétines et du patrimoine de St-Pierre, à savoir les principautés de Capoue, Bénévent et
                                                Salerne, ainsi que les duchés de Naples, Gaète, Amalfi et Sorrente.
                                                La première partie de l’ouvrage propose une mise au point sur la généalogie des familles
                                                dominantes en Italie du sud, ainsi qu’une étude des alliances matrimoniales. On donne
                                                également des pistes quant à l’origine familiale des différentes lignées régnantes, notamment
                                                grâce à l’onomastique et à la chronologie comparative, et on reconstitue les branches cadettes
                                                des différents troncs princiers et ducaux.</p>

                                            <p>La prosopographie proprement dite donne une notice biographique pour chacune des dames
                                                recensées, avec les principales sources et la bibliographie existante. On a identifié 375 femmes
                                                pour les huit lignages. La plupart sont issues d’un de ces lignages et mariées dans un autre.
                                                Elles sont prioritairement recensées dans le lignage auquel elles appartiennent par mariage: par
                                                exemple Adelchisa, soeur de Guaifier de Salerne et épouse de Sicard de Bénévent, est reprise
                                                dans le chapitre qui traite des princes de Bénévent, bien que par la naissance elle appartienne
                                                au genus de Salerne. Gaitelgrima, fille de Pandulf II de Bénévent (III de Capoue), est
                                                mentionnée dans le chapitre consacré à la seconde lignée des princes de Salerne, car elle avait
                                                épousé Gaimar III de Salerne. C’est pourquoi on trouvera en fin de volumes des tableaux
                                                généalogiques détaillés.</p>


