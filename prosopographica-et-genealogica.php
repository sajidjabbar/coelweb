                                            <p>‘What’s in a name?’ is an unjustly neglected question to which historians have recently turned
                                                their attention. A number of groups are now studying a range of questions, following the
                                                evolution of the Germanic one-name system to the two-name system of the central middle Ages
                                                and the development of the modern three-name system. Name-giving and its rationale is of
                                                great importance in the study of any society, especially one as dominated by the family as the
                                                Middle Ages clearly was. The essays collected in this volume range widely from early
                                                medieval Ireland through France and Germany to the Holy Land exploring the relationship
                                                between families and their name-giving strategies. Most of the papers were presented at two
                                                round tables held in France at Saint-Jean d’Angely and in the Institut Historique Allemand de
                                                Paris under the presidence, respectively, of Szabolcs de Vajay and Karl Ferdinand Werner</p>
