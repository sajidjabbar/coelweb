                                        <p>This book is the translation into French and publication in final form of a doctoral thesis
                                            presented at Manheim. To understand its importance, one need only note that, although it has
                                            been difficult to access up till now, it is the work most often cited by historians as the only
                                            reference work for the serious study of the Poitevin nobility of the Early Middle Ages. Jan Prell
                                            undertook painstakingly detailed research in order to identify the élites of one of the principal
                                            Frankish provinces at the turn of the year 1000. The precise identification of those who acted
                                            on behalf of the duke allowed him to trace in detail the politics of the first dukes of Aquitaine
                                            and the way in which they were able to exercise power. For if this is an outstanding work of
                                            prosopography, it is far more than that. It is a rich assemblage of data relating to the history of
                                            Poitou at the end of the Carolingian period and one that fills a void that until now has existed
                                            in the history of medieval France. The study of lineages and their interaction at the heart of
                                            Poitou – and also with other contemporary Frankish provinces – sheds new light on the history
                                            of a crucial period in the creation of regional identities</p>
                                        <p>Jan Hendrik PRELL (died January 2004), former research assistant at the Bayerische
                                            Staatsbibliothek de Munich, submitted in 1992 a thesis directed by K.F. Werner entitled Graf,
                                            Vizegrafen und Adel in Nordaquitanien (10. und 11. Jahrhundert). Verfassungs-geschichtlichprosopographische Untersuchungen zu Poitou, Aunis und Saintonge. He also published several
                                            articles, both in edited collections and in prestigious journals such as the Bibliothèque de
