                                                Aquitaine or Languedoc from the ninth to the eleventh century, notably in the Toulousain,
                                                Périgord, Limousin, Poitou and Auvergne. For each of these regions, the author focuses on
                                                problems linked to the study of the dominant families. Hence one will find a detailed study on
                                                the alliances of the counts of Toulouse, with a particular attention to the succession and on
                                                distinguishing between the various homonyms who succeeded one another as head of the
                                                family during the tenth century. For Périgord, the author painstakingly re-examines the
                                                complex issues surrounding the establishment of the filiation of the counts, as well as paying
                                                detailed attention to the previously neglected issue of the origins of their wives. Limousin is
                                                examined in terms of the links between the various viscomital families between which the
                                                region was divided. The opportunity is taken to examine in detail the genealogy of saint Gerald
                                                of Aurillac, whose contemporary biographer linked him with certain great saints of the
                                                Merovingian era, as well as to analyze the value of a twelfth-century genealogy which allegedly
                                                describes the ancestry of one of these saints over ten generations. The study of Poitou offers,
                                                in anticipation of the publication in this same collection of J. H. Prell’s thesis, an account of
                                                the viscounts of the families of Thouars, Chatellerault and Aulnay, their alliances and their
                                                links with the other great families. Finally, the author revisits some little studied questions
