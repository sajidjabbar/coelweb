                                            <p>L’ascendance de Charlemagne n’a cessé depuis le IXe siècle de fasciner généalogistes et
                                                historiens. L’envergure du personnage peut certes expliquer cette fascination. Mais
                                                Charlemagne, père de l’Europe, est aussi l’ancêtre de la plupart des Européens, de sorte que
                                                ses ascendants sont aussi les nôtres.
                                                C’est le propos de cet ouvrage, refonte complète d’un livre publié en 1989, d’apporter des
                                                réponses aux nombreuses questions restées en suspens depuis des siècles.
                                                Au terme d’une enquête minutieuse et passionnante, le lecteur découvrira les parentés
                                                certaines ou probable de l’empereur : ses liens avec la dynastie des Mérovingiens en Gaule,
                                                les dynasties des autres royaumes germaniques ou les familles sénatoriales romaines.
                                                Christian Settipani, docteur en Histoire, est spécialiste de la continuité des familles du Haut
                                                Moyen Âge et de l’Antiquité et a publié de nombreux autres travaux sur ces questions.
