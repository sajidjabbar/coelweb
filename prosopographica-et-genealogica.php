                                            <p>Cet ouvrage se propose de faire le point sur la continuité du sénat romain à l’époque impériale.
                                                Le taux de renouvellement important du sénat romain est un phénomène mis en évidence
                                                depuis longtemps par les historiens. Pourtant certaines familles y sont attestés sur une longue
                                                durée, quelquefois sur plusieurs siècles. L’auteur s’interroge alors sur les mécanismes de cette
                                                continuité, en particulier sur le rapport qui existe entre la continuité du nom et celle de la
                                                famille. Le nom peut se transmettre en dehors du cercle agnatique, et la famille peut disparaître
                                                socialement et se poursuivre biologiquement. La plupart du temps, les sources ne
                                                permettent d’appréhender que la perpétuation du nom (gentilice), e- confusion. Après l’étude
                                                des facteurs de rupture et des atouts de la continuité, on trouvera l’analyse prosopographique
                                                et généalogique de plusieurs groupes connus sur une durée importante. Les diverses modalités
                                                de transmission des noms au sein de ces groupes seront ainsi mises en valeur</p>
