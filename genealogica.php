                                            <blockquote>
                                                Essentially the family of Charlemagne’s queen Hildegard, the Konradiner
                                                were the first non-Carolingian dynasty to succeed to the East Frankish
                                                throne, and at various points during the tenth century they held ducal
                                                office in Franconia, Lotharingia, Swabia and Alsace. The genealogical
                                                reconstruction of the Konradiner is widely disputed. While the
                                                reconstruction proposed in this book was first developed in 1990, the
                                                base of the inquiry has been broadened and new sources and materials are
                                                applied. These studies represent an introduction to, and partial
                                                description of, the early medieval law of succession to public office.
                                                Inheritance of counties in the Hessian region is investigated in
                                                extensive detail. Further topics include the development of margravial
                                                offices in the West Frankish kingdom and Thuringia, and the passage of
                                                the imperial battle standard from the eighth to the twelfth century.
