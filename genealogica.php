                                        <div class="text-box">
                                            <p>This book studies the careers of the descendants of five important religious élite families of the Hijāz for the
                                                Umayyad and early ‘Abbāsid periods (40-218 AH). The eponym of each family was among the earliest and most
                                                celebrated converts to Islam and each was a viable candidate for the caliphate after the murder of ‘Umar I (d.
                                                23 AH). Yet, though two among them did rule as caliphs in a highly volatile milieu, no direct descendant of
                                                theirs was ever able to assume the supreme rule of the Muslim polity after them. What happened to these highly
                                                prominent families? This book presents the sociopolitical trajectories of these élite families.</p>
                                            <p>The Arabo-Islamic historiographical tradition not only ignores the sociopolitical history of the early Islamic
                                                Hijāz, but it also confronts the historian with many insurmountable technical challenges. The problems of the
                                                paucity of the desired narrative historiography and the notoriously unwieldy sources can be overcome if
                                                provincial history is made to depend on prosopography, i.e. if it is taken to be a history of individuals
                                                belonging to identifiable groups. Insofar as they belong to increasingly wider categories—family branch, family,
                                                clan, tribe, etc.—that have a diachronic presence, a detailed and reliable sociopolitical history of the Hijāz
                                                can be reconstructed. This is what this book aims to do in a preliminary way on the basis of the five families.
                                            </p>
                                            <p>The body of the book addresses three subjects: (1) the potential use and abuse of Arabo-Islamic genealogies and
                                                prosopographies for writing provincial histories; (2) kinship and marriage in early Islamic Arabia generally
                                                and, more specifically, the importance of cognate and marital links for social ascendancy and for the creation
                                                of quasi-corporate political blocs that cut across tribal and agnatic lines; and (3) the social and political
                                                history of the Hijāz. As for the last point, in summary, this book shows that, with a few exceptions, ‘Abd
                                                al-Malik (r. 65-86 AH) had been able to reconcile with the Hijāzī élite by the end of the counter-caliphate of
                                                ‘Abdallāh b. al-Zubayr (r. ca. 63-72 AH), plying them, as Mu‘āwiya (r. 40-60 AH) had once done, with gifts and
                                                political posts. As his program of centralization became more oppressive in his last years, so too did his
                                                relationship with an increasingly disenfranchised Hijāzī élite become more strained. By the end of the reign of
                                                al-Walīd (r. 86-96 AH), the Hijāzī élite had mobilized again, this time forming two blocs—one under the
                                                leadership of the Hasanid ‘Alids and the other under the initial joint leadership of the ‘Abbāsids, Husaynids,
                                                and Hanafiyya. It is the movement of these two groups that culminated in the demise of the Umayyads (40-132 AH),
                                                after which the élite structures were again reshuffled. In the early ‘Abbāsid period, the marginalized élite who
                                                had once been attracted to the Hasanid block were again absorbed into government service in the Hijāz. During
                                                this time, one also begins to notice the gradually increasing cooperation of the various ‘Alid lines.</p>
