                                            <p>‘What’s in a name?’ is an unjustly neglected question to which historians
                                                have recently&nbsp;turned their attention. A number of groups are now
                                                studying a range of questions,&nbsp;following the evolution of the
                                                Germanic one-name system to the two-name system of&nbsp;the central
                                                middle Ages and the development of the modern three-name
                                                system.&nbsp;Name-giving and its rationale is of great importance in the
                                                study of any society,&nbsp;especially one as dominated by the family as
                                                the Middle Ages clearly was. The essays&nbsp;collected in this volume
                                                range widely from early medieval Ireland through France&nbsp;and Germany
                                                to the Holy Land exploring the relationship between families and
                                                their&nbsp;name-giving strategies. Most of the papers were presented at
                                                two round tables held&nbsp;in France at Saint-Jean d’Angely and in the
                                                Institut Historique Allemand de Paris&nbsp;under the presidence,
                                                respectively, of Szabolcs de Vajay and Karl Ferdinand Werner.&nbsp;The
                                                participants have since launched the Society for Onomastic and Kinship
                                                Studies&nbsp;in the medieval West.&nbsp;</p>
