                                            <p>‘Que signifie le nom ‘?’ est la question trop souvent négligée vers
                                                laquelle les historiens ont récemment porté leur attention. Plusieurs
                                                équipes se penchent désormais sur un certain nombre de questions
                                                concernant l’évolution du mode de dénomination du nom unique germanique
                                                et de son passage au système à deux noms du moyen âge central puis à son
                                                développement au système actuel à trois noms. Les raisons qui président
                                                à la dévolution d’un nom sont d’une importance cruciale dans l’étude de
                                                toutes les sociétés, et plus particulièrement pour celles fondées sur la
                                                famille, comme c’était le cas au Moyen Age. Les travaux réunis dans ce
                                                volume vont de l’Irlande médiévale à la France et à l’Allemagne, et
                                                jusqu’à la Terre Sainte. Ils explorent les relations de parenté entre
                                                familles et leur stratégies onomastiques.

                                                La plupart de ces articles ont été présentés à l’occasion de deux tables
                                                rondes tenues en France, à Saint-Jean d’Angely et à l’institut
                                                Historique Allemand de Paris, sous la présidence respective de Szabolcs
                                                de Vajay et de Karl Ferdinand Werner. Les participants ont depuis crée
